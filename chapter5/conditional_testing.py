# conditional testing 

car = 'subaru'

print("Is car == 'subaru'? I predict TRUE")
print(car == 'subaru')

print("Is car == 'audi'? I predict FALSE")
print(car == 'audi')

# new conditional
# A single '=' states a variable
pizza_ingredient = 'ham'

print("\nIs pizza_ingredient == 'ham'? I predict True")
# the "==" aks whether the variable is the same as the result, if it is, it will be TRUE
print(pizza_ingredient == 'ham')

print("Is pizza_ingredient == 'cheese'? I predict False")
print(pizza_ingredient == 'cheese')